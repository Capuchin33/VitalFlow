/** Nested messages; access with dot paths, e.g. `nav.data`. */
export type MessageTree = Record<string, string | Record<string, unknown>>;

function flattenTree(tree: Record<string, string | Record<string, unknown>>, prefix = ""): Record<string, string> {
  const out: Record<string, string> = {};
  for (const [k, v] of Object.entries(tree)) {
    const path = prefix ? `${prefix}.${k}` : k;
    if (typeof v === "string") {
      out[path] = v;
    } else if (v && typeof v === "object") {
      Object.assign(out, flattenTree(v as Record<string, string | Record<string, unknown>>, path));
    }
  }
  return out;
}

